1、	个人信息：是指以电子或者其他方式记录的能够单独或者与其他信息结合识别特定自然人的各种信息，包括自然人的姓名、出生日期、身份证件号码、生物识别信息、住址、电话号码、电子邮箱、健康信息、行踪信息等。
2、	易企聊企业/机构用户（“您的组织”）：指通过开户、认证、经由其管理员用户的易企聊帐号上传企业/机构成员通讯录搭建企业/机构组织/管理架构，开通、管理和使用易企聊产品或服务的企业/机构。易企聊企业/机构用户包括但不限于法人、政府机构、其他组织、合伙或个体工商户等。
易企聊企业/机构管理员用户（“管理员用户”）：指经您的组织合法授权或指定而开通管理员账号的个人用户，该管理员账号拥有您的组织的易企聊平台中管理操作权限，代表您的组织上传您的组织的成员通讯录等数据，并开通、管理和使用易企聊产品或服务；管理员用户可根据您的组织的实际需要设立多层级管理员用户，在其享有的权限之下搭建多层级管理员架构并逐级设置相应的管理权限。
易企聊企业/机构授权成员用户（“授权成员用户”）：是指您的组织为使上传之成员通讯录（包括成员账号名、工作或组织事务用电邮、姓名、电话、所属部门及职位等）中的成员使用易企聊产品或服务，而由管理员用户直接为该等成员开设并分发给该等成员相应的易企聊个人账号，因该等开设、分发而持有并使用此类易企聊账号的个人用户。
上述管理员用户及授权成员用户，以下合称为“您”。
3、	您已知悉，您的组织为您使用易企聊产品或服务而开设、分发给您相应的易企聊账号，开通易企聊产品或服务的相关功能，为您提供或控制您使用该等产品或服务的权限，并对您使用易企聊产品或服务的行为进行管理；在上述过程中，您的组织将代表您、或您将按照您的组织的要求向本公司提供或可能提供与您相关的若干个人信息，包括账号名、工作或组织事务电邮、姓名、电话、所属部门及职位等。
您的组织已经或在此陈述和保证，其为上述信息的控制方，其通过管理员账号向本公司提供您的任何个人信息或者要求您提供任何个人信息之前，已经事先获得您的明确授权，并已经充分告知您收集您的个人信息的目的、范围、使用方式，其收集您个人信息的范围仅限于实现本协议目的必要范围。如您并不知悉并同意本款内容，请立即停止使用易企聊产品或服务，并与您的组织就前述事项进行确认。对于上述信息，本公司仅作为数据处理者根据您的组织的要求处理您的个人信息，我们对您的组织收集和使用您个人信息的行为不承担任何法律责任；请及时与您的组织沟通以详细了解对您的个人信息的控制、保护等相关政策或规定。
4、	未成年人保护
4.1	易企聊产品或服务针对企业/机构的需求而设立，除特殊行业机构（比如教育机构）外，原则上未满18周岁的未成年人不应成为易企聊产品及服务的个人用户。
4.2	若您是未满18周岁的未成年人，在符合适用法律的前提下成为易企聊产品及服务的用户时，您的组织应在提供成员通讯录时明示，且您在使用易企聊产品及服务前，应在您的父母或其他监护人监护、指导下共同阅读并同意本隐私政策。
4.3	本公司根据适用法律法规的规定保护未成年人的个人信息，只会在法律法规允许、父母或其他监护人明确同意或保护未成年人所必要的情况下收集、使用、储存、共享、转让或披露未成年人的个人信息；如果本公司发现在未事先获得可证实的父母同意的情况下收集了未成年人的个人信息，则会设法尽快删除相关信息。
4.4	若您是未成年人的监护人，当您对您所监护的未成年人的个人信息的收集、使用等有相关疑问时，请立即通过本隐私政策公示的联系方式与本公司联系。
4.5	若涉及儿童（即不满十四周岁的未成年人）使用易企聊产品或服务的，您的组织应在提供成员通讯录之前或之时、或者您的组织开户前向本公司明示，本公司将依据儿童个人信息保护的相关法律法规之规定，设立并执行特殊的保护规定、政策及保护机制。
5、	本隐私政策不适用于其他第三方通过易企聊客户端直接向您提供的产品或服务（统称“第三方服务”）。如管理员用户通过易企聊平台开通并由您确认使用任何第三方应用服务，您的组织代表您或您向该第三方服务提供者提供的信息不适用于本隐私政策，而适用于您与该等第三方服务提供者之间的相关政策或规定。您在选择使用该等第三方服务前，请充分了解第三方服务的产品功能及隐私保护政策，再选择是否开通功能。

二、	如何收集和使用您的个人信息

(一)	收集用户信息
为了向您及您的组织提供易企聊产品或服务，维护易企聊产品或服务的正常运行，改进及优化相应的用户体验并保障您的账号安全，本公司会出于本隐私政策下述目的及方式收集您在注册、使用易企聊产品或服务时主动提供、授权提供或基于您的组织要求提供的信息，以及基于您使用易企聊产品或服务时产生的信息：

1、	开户及注册个人账号过程中收集信息
1)	注册授权成员用户
为注册成为易企聊授权成员用户，您的组织将通过本隐私政策第一条2、3款中的方式向本公司提供您的若干个人信息，包括账号名、工作或组织事务用电邮、姓名、电话、所属部门及职位等，用以注册并创建您的易企聊个人账号，否则您将不能使用易企聊产品或服务。
2)	开户及注册管理员用户
为使用易企聊的产品或服务，您的组织在向本公司申请开户时将向本公司提供企业/机构名称、统一社会信用代码以及联系人信息等。开户并经分配产生管理员账号后，在您使用易企聊相关产品或服务前，您的组织应完成相应的实名认证。进行实名认证时，您作为您的组织的管理员，需代表您的组织根据页面指示继续提供特定信息和资料，包括但不限于您的组织的营业执照（或起到同等功能的资质证照）复印件、统一社会信用代码、企业组织名称、地址、法人代表（或负责人）姓名、法人代表（负责人）身份证号、指定管理员的合规授权委托、指定管理员的身份证号等基本信息。您理解并同意，您在向本公司提供上述相关指定管理员、法定代表人（负责人）等人员的该等个人信息之前，您需确保您已经取得其授权同意。
3)	开通易企聊产品或服务功能
基于您的组织在劳动、事务管理、办公应用等方面需要，管理员用户选择开通易企聊提供的产品或服务时，根据使用该等产品或服务的必须而涉及需要由您的组织代表您或您按照您的组织的要求提供其他相应的个人信息。

2、	使用易企聊产品或服务过程中收集信息
当您在使用易企聊产品或服务、您在使用我们服务过程中为获得技术支持、协助、改进及优化易企聊产品或服务的用户体验、以及保障账号安全等，而向本公司提供或本公司收集的用户个人信息，包括当不限于： 
1)	使用信息
我们会收集您使用易企聊产品或服务时提交或产生的信息。如您使用易企聊进行即时通讯（IM）聊天，我们需要记录您的聊天信息及语音通话状态信息（非通话内容）以便实现信息的传输与交互。
2)	设备、日志信息
您在使用易企聊产品或服务过程中，我们会根据您在软件安装及使用中授予的具体权限，接收并记录您所使用的设备相关信息（例如设备型号、操作系统版本、设备设置、唯一设备标识符等软硬件特征信息）、设备所在位置相关信息（例如IP地址以及移动装置地址设定的地点信息、以及GPS所得地点信息（如果您的移动装置启动该功能）等，且只在您需要使用时）。
当您使用易企聊产品或服务时，我们会自动收集您对我们服务的详细使用情况，作为有关网络日志保存。例如您的搜索查询内容、IP地址、浏览器的类型、电信运营商、使用的语言、访问日期和时间、您访问的网页记录、通话状态信息、交易日志信息等操作日志信息。
为明确起见，本公司在供服务的过程中可能收集或使用的非识别性信息（该等非识别性信息，无法单独用于确定特定自然人身份），比如单独的设备信息、日志信息等，或者本公司收集汇总的统计数据等，是无法识别特定自然人身份的信息；该等非识别性信息不属于个人信息。但是，如果我们将这类非个人信息与其他信息结合用于识别特定自然人身份，或者将其与个人信息结合使用，则在结合使用期间，这类非个人信息将有可能被视为个人信息，除取得您的授权或相关法律法规另有规定外，我们会将该类个人信息做匿名化、去标识化处理。
3)	为实现您的组织的管理功能在使用易企聊相关功能/应用过程中提交或产生的信息
管理员用户选择开通、管理易企聊相关产品或服务的，我们会根据管理员用户的指令记录您使用易企聊相关功能/应用过程中提交或产生的信息和数据：该等信息和数据中涉及的个人信息，您知悉并同意，您的组织作为上述信息和数据的控制者，我们仅根据管理员用户的指令管理和使用上述数据和信息，其中涉及您的个人信息的，则仍是受限于本协议第一条第3款的约定。
4)	体验反馈、客服及争议处理中您向我们提供的信息
在易企聊产品或服务使用过程中，您可以对易企聊的产品、功能、应用及服务的体验反馈问题，帮助我们更好地了解您使用我们产品或服务的体验和需求，改善我们的产品或服务,为此我们会记录您的联系信息、反馈的问题或建议，以便我们进一步联系您反馈我们的处理意见。
当您与我们联系或提出售中售后、争议纠纷处理申请时，为了保障您的账号及系统安全，我们需要您提供必要的个人信息以核验您的用户身份。为便于与您联系、尽快帮助您解决问题或记录相关问题的处理方案及结果，我们可能会保存您与我们的通信/通话记录及相关内容（包括账号信息、工单信息、您为了证明相关事实提供的其他信息，或您留下的联系方式信息）。
5)	第三方向我们提供的信息
我们将对其他用户进行与您相关的操作时收集关于您的信息，亦可能从本公司的关联公司、合作第三方或通过其他合法途径获取的您的个人信息。
6)	其他
为向您提供更便捷、更优质、个性化的产品及/或服务，努力提升您的用户体验，我们在向您提供的以下服务中可能会收集和使用您的个人信息。
比如，基于麦克风的语音技术相关服务：您可在开启麦克风权限后使用麦克风实现在易企聊内的单聊/群聊中语音输入功能，或与客服联系或与客服机器人实现语音交互。请您知晓，即使您已同意开启麦克风权限，我们也仅会在您主动点击客户端内麦克风图标或录制视频时通过麦克风获取语音信息。您理解并同意，该等服务可能需要您在您的设备中开启麦克风（语音）的访问权限，以实现这些权限所涉及信息的收集和使用。您可在您的客户端“我的-设置-通用或隐私”中查看上述权限的状态，并可自行决定这些权限随时的开启或关闭。请您注意，您开启任一权限即代表您授权我们可以收集和使用相关个人信息来为您提供对应服务，您一旦关闭任一权限即代表您取消了授权，我们将不再基于对应权限继续收集和使用相关个人信息，也无法为您提供该权限所对应的服务。但是，您关闭权限的决定不会影响此前基于您的授权所进行的信息收集及使用。
