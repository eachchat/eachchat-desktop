三、	如何使用 Cookie 和同类技术

您使用易企聊产品或服务时，我们使用Cookie或其他类似技术（以下简称“Cookie”），以提升您使用易企聊产品或服务的体验。即在您使用易企聊的产品或服务时，我们可能用相关技术向您的设备发送一个或多个Cookie或匿名标识符，以收集和存储您访问、使用易企聊产品或服务时的信息。借助于Cookie，在您访问或再次访问易企产品或服务时,我们能识别您的身份，并通过分析数据为您提供更好的服务。这么做是为了解您的使用习惯，帮您省去重复输入账号信息的步骤，或者帮助判断您的账号安全。
我们不会将Cookie用于本隐私政策所述目的之外的其他用途；您可根据自己的偏好管理或删除Cookie；大部分网络浏览器自动接受Cookie，但具体是否接受由您自行决定。如需详细了解如何更改浏览器设置，请访问您使用的浏览器的相关设置页面。您知悉并同意，如果您选择禁用Cookie，则需要在每一次使用易企聊产品或服务时更改用户设置或者可能导致某些依赖于Cookie的服务或功能不能使用，由此导致的后果及责任由您自行承担。

四、	如何共享、转让、公开披露您的信息

(一)	共享
我们不会与任何公司、组织和个人共享您的个人信息，但以下情况除外：
1、	在获得您的明确同意后，我们会与其他方共享您的个人信息。
2、	我们可能会根据适用法律法规规定，诉讼、仲裁等争议解决程序需要，或按相关行政、司法机关或其他监管机构依法提出的要求，对外共享您的个人信息。
3、	基于您的主动选择，实现功能或服务的共享：只有共享您的个人信息、才能提供您所要求的第三方产品和服务，如：
1)	您的组织的管理员用户选择开通、管理并使用第三方应用服务，将代表您同意我们向该第三方应用服务提供商提供您的组织基于雇佣和/或管理行为控制的您个人基础信息（账号名、姓名、头像、电话、工作或组织事务用电邮、所属部门及职位等信息）及基础权限；管理员用户开通使用第三方应用服务时，需阅读并充分了解并遵守第三方应用服务的产品功能、声明及相应隐私保护政策。
2)	经您同意，当您使用易企聊账号登录第三方产品或服务时、或将账号与其他第三方账号绑定时，我们会将您的基础信息（姓名、头像）及其他经您授权的信息与该等第三方产品或服务共享。
